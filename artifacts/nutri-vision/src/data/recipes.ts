export type BMICategory = "Underweight" | "Normal" | "Overweight" | "Obese";
export type Goal = "Weight Loss" | "Maintain" | "Weight Gain";

export function getBMICategory(bmi: number): BMICategory {
  if (bmi < 18.5) return "Underweight";
  if (bmi < 25) return "Normal";
  if (bmi < 30) return "Overweight";
  return "Obese";
}

export function getFilteredRecipes(
  ingredients: string[],
  bmiCategory: BMICategory,
  goal: Goal
): Recipe[] {
  if (ingredients.length === 0) return [];
  return RECIPES.filter(
    (r) =>
      ingredients.includes(r.ingredient) &&
      r.bmi.includes(bmiCategory) &&
      r.goals.includes(goal)
  );
}

export function getRandomDetectedIngredients(): string[] {
  const shuffled = [...VEGETABLE_OPTIONS].sort(() => Math.random() - 0.5);
  const count = Math.floor(Math.random() * 3) + 2; // 2–4
  return shuffled.slice(0, count);
}
